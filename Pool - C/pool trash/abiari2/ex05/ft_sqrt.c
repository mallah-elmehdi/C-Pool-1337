int		ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb > 46340 * 46340)
		return (0);
	if (nb == 1)
		return (1);
	while (i <= nb / 2)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
