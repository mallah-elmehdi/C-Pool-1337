void	ft_print_alphabet(void)
{
	char	a;
	int		i;

	a = 'a';
	i = 1;
	while (i <= 26)
	{
		ft_putchar(a);
		i++;
		a++;
	}
}

int		main()
{
	ft_print_alphabet();
	return 0;
}
