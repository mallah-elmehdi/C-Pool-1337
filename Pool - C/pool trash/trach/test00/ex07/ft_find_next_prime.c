int		ft_find_next_prime(int nb)
{
	int a;

	a = 2;
	if (nb >= 0 && nb <= 2)
	{
		return (2);
	}
	else
	{
		while (a < nb)
		{
			if ((nb % a) == 0)
			{
				nb++;
				a = 1;
			}
			a++;
		}
	}
	return (nb);
}
